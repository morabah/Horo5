export function deriveLegacySubfeelingSlug(productSlug: string, feelingSlug: string) {
  const normalized = productSlug.toLowerCase();

  if (normalized.startsWith('emotions-')) {
    if (normalized.includes('raw') || normalized.includes('shattered')) return 'i-dont-care';
    if (normalized.includes('silent') || normalized.includes('unspoken')) return 'i-care';
    if (normalized.includes('deep')) return 'overthinking';
    return 'numb';
  }

  return DEFAULT_SUBFEELING_BY_FEELING[feelingSlug] ?? 'i-care';
}

